#include <string>
#include <vector>

/**
 * Splits string into vector of string removing spaces
 * @param str input to convert
 * @return vector of integers from input string
 */
auto string_to_vec_int(std::string str) -> std::vector<int>;

/**
 * Converts string to single integer
 * @param str input to convert
 * @return integer from input string
 */
auto string_to_int(std::string str) -> int;